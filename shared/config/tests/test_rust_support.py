class TestRustDetection:
    """Tests for Rust language detection."""
    
    def test_detect_rust_from_cargo_toml(self, tmp_path):
        """Test detecting Rust from Cargo.toml file."""
        cargo_toml = tmp_path / "Cargo.toml"
        cargo_toml.write_text("""
[package]
name = "my-crate"
version = "0.1.0"
edition = "2021"

[dependencies]
serde = "1.0"
""")
        
        result = detect_language(tmp_path)
        assert result.primary_language == Language.RUST
        assert Language.RUST in result.detected_languages
        assert "Cargo.toml" in result.markers_found[Language.RUST]
    
    def test_detect_rust_from_rs_files(self, tmp_path):
        """Test detecting Rust from .rs source files."""
        src_dir = tmp_path / "src"
        src_dir.mkdir()
        (src_dir / "main.rs").write_text("fn main() { println!(\"Hello\"); }")
        (src_dir / "lib.rs").write_text("pub fn add(a: i32, b: i32) -> i32 { a + b }")
        
        result = detect_language(tmp_path)
        assert result.primary_language == Language.RUST
        assert "*.rs" in result.markers_found[Language.RUST]
    
    def test_detect_rust_from_cargo_lock(self, tmp_path):
        """Test detecting Rust from Cargo.lock file."""
        (tmp_path / "Cargo.lock").write_text("""
# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 3

[[package]]
name = "my-crate"
version = "0.1.0"
""")
        
        result = detect_language(tmp_path)
        assert result.primary_language == Language.RUST
        assert "Cargo.lock" in result.markers_found[Language.RUST]


class TestCargoWorkspace:
    """Tests for Cargo workspace detection."""
    
    def test_detect_cargo_workspace(self, tmp_path):
        """Test detecting Cargo workspace structure."""
        cargo_toml = tmp_path / "Cargo.toml"
        cargo_toml.write_text("""
[workspace]
members = ["crate1", "crate2", "crate3"]
resolver = "2"
""")
        
        # Create member directories
        (tmp_path / "crate1").mkdir()
        (tmp_path / "crate2").mkdir()
        (tmp_path / "crate3").mkdir()
        
        result = detect_monorepo_structure(tmp_path)
        assert result.is_monorepo is True
        assert result.workspace_type == "cargo"
        assert "crate1" in result.packages
        assert "crate2" in result.packages
        assert "crate3" in result.packages
        assert result.language_per_package["crate1"] == Language.RUST
    
    def test_detect_cargo_workspace_with_paths(self, tmp_path):
        """Test detecting Cargo workspace with path patterns."""
        cargo_toml = tmp_path / "Cargo.toml"
        cargo_toml.write_text("""
[workspace]
members = ["packages/*", "tools/cli"]
""")
        
        # Create member directories
        packages_dir = tmp_path / "packages"
        packages_dir.mkdir()
        (packages_dir / "core").mkdir()
        (packages_dir / "utils").mkdir()
        
        tools_dir = tmp_path / "tools" / "cli"
        tools_dir.mkdir(parents=True)
        
        result = detect_monorepo_structure(tmp_path)
        assert result.is_monorepo is True
        assert result.workspace_type == "cargo"


