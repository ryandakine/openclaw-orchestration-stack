        assert "cargo test" in config.commands.test
        assert "cargo clippy -- -D warnings" in config.commands.lint
        # Note: cargo fmt --check is in format section, not lint
        assert "cargo fmt --check" in config.commands.format
        assert "cargo audit" in config.security.dependency_scan


